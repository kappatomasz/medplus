    private void setCharacterProperty(Object style, Object value){
        StyledDocument sd = patientLogPanel.getDescriptionTxtField().getStyledDocument();
           
        Element element = sd.getCharacterElement(patientLogPanel.getDescriptionTxtField().getCaretPosition());
        AttributeSet attributes = element.getAttributes();
        StyleContext sc = StyleContext.getDefaultStyleContext();
            
        attributes = sc.addAttribute(attributes, style, value);
        
        int length = patientLogPanel.getDescriptionTxtField().getSelectionEnd() - patientLogPanel.getDescriptionTxtField().getSelectionStart();
        if(length<0){
           length = 0;
        }
        sd.setCharacterAttributes(patientLogPanel.getDescriptionTxtField().getSelectionStart(), length, attributes, true);
        patientLogPanel.getDescriptionTxtField().setStyledDocument(sd);
    }
    
    private void removeCharacterProperty(Object style){
        StyledDocument sd = patientLogPanel.getDescriptionTxtField().getStyledDocument();
           
        Element element = sd.getCharacterElement(patientLogPanel.getDescriptionTxtField().getCaretPosition());
        AttributeSet attributes = element.getAttributes();
        StyleContext sc = StyleContext.getDefaultStyleContext();
            
        attributes = sc.removeAttribute(attributes, style);
        int length = patientLogPanel.getDescriptionTxtField().getSelectionEnd() - patientLogPanel.getDescriptionTxtField().getSelectionStart();
        if(length<0){
           length = 0;
        }
        sd.setCharacterAttributes(patientLogPanel.getDescriptionTxtField().getSelectionStart(), length, attributes, true);
        patientLogPanel.getDescriptionTxtField().setStyledDocument(sd);
    }
    
    private void changeFont(Font font){
        patientLogPanel.getDescriptionTxtField().setFont(font);
    }
    
    private void initializeFonts(){
        GraphicsEnvironment ge = GraphicsEnvironment.getLocalGraphicsEnvironment();
        Font[] fonts = ge.getAllFonts();
        fontComboBoxModel = new DefaultComboBoxModel<>();
        fontSizeComboBoxModel = new DefaultComboBoxModel<>();
        for(Font font : fonts){
            fontComboBoxModel.addElement(new FontComboBoxModel(font));
        }  
        fontComboBox.setModel(fontComboBoxModel);
        fontSizeCombo.setModel(fontSizeComboBoxModel);
        fontSizeCombo.removeAllItems();
        
        for (int i=8; i<72; i=i+2){
            fontSizeCombo.addItem(new Integer(i));
        }
        
    }

