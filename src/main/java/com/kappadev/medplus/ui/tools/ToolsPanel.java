    private void exportBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_exportBtnActionPerformed
        DataMigrationWindow dmw = new DataMigrationWindow(DataMigrationConstants.DATA_MIGRATION_EXPORT);
        dmw.setVisible(rootPaneCheckingEnabled);
        DiskDrive dd = dmw.getSelectedDiskDrive();
        if(dd.getPath()!=null){     
              File databaseFileInput = new File(System.getProperty(DataMigrationConstants.USER_HOME_DIR)+File.separator+DataMigrationConstants.DATA_MIGRATION_DATABASE_FILE);
              File databaseTraceFileInput = new File(System.getProperty(DataMigrationConstants.USER_HOME_DIR)+File.separator+DataMigrationConstants.DATA_MIGRATION_DATABASE_TRACE_FILE);
              File databaseFileOutput =  new File(dd.getPath()+File.separator+DataMigrationConstants.DATA_MIGRATION_DATABASE_FILE);
              File databaseTraceOutput = new File(dd.getPath()+File.separator+DataMigrationConstants.DATA_MIGRATION_DATABASE_TRACE_FILE);
              CopyUtil databaseFiles = new CopyUtil(databaseFileInput, databaseFileOutput, progressBar);
              fileNameLabel.setText(DataMigrationConstants.DATA_MIGRATION_DATABASE_FILE);
              databaseFiles.execute();
              CopyUtil databaseTraceFiles = new CopyUtil(databaseTraceFileInput, databaseTraceOutput, progressBar);  
              fileNameLabel.setText(DataMigrationConstants.DATA_MIGRATION_DATABASE_TRACE_FILE);
              databaseTraceFiles.execute();
        }
    }//GEN-LAST:event_exportBtnActionPerformed

