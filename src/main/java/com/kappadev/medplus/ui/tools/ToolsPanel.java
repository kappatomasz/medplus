    private void importBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_importBtnActionPerformed
        DataMigrationWindow dmw = new DataMigrationWindow(DataMigrationConstants.DATA_MIGRATION_IMPORT);
        dmw.setVisible(rootPaneCheckingEnabled);
        DiskDrive dd = dmw.getSelectedDiskDrive();
        if(dd.getPath()!=null){
           File medRegistryDir = new File(dd.getPath()+File.separator+DataMigrationConstants.DATA_MIGRATION_FILES_FOLDER);
           if(medRegistryDir.exists()){
              File databaseFileInput = new File(dd.getPath()+File.separator+DataMigrationConstants.DATA_MIGRATION_DATABASE_FILE);
              File databaseTraceFileInput = new File(dd.getPath()+File.separator+DataMigrationConstants.DATA_MIGRATION_DATABASE_TRACE_FILE);
              File databaseFileOutput = new File(System.getProperty(DataMigrationConstants.USER_HOME_DIR)+File.separator+DataMigrationConstants.DATA_MIGRATION_DATABASE_FILE);
              File databaseTraceOutput = new File(System.getProperty(DataMigrationConstants.USER_HOME_DIR)+File.separator+DataMigrationConstants.DATA_MIGRATION_DATABASE_TRACE_FILE);
              CopyUtil databaseFiles = new CopyUtil(databaseFileInput, databaseFileOutput, progressBar);
              fileNameLabel.setText(DataMigrationConstants.DATA_MIGRATION_DATABASE_FILE);
              databaseFiles.execute();
              CopyUtil databaseTraceFiles = new CopyUtil(databaseTraceFileInput, databaseTraceOutput, progressBar); 
              fileNameLabel.setText(DataMigrationConstants.DATA_MIGRATION_DATABASE_TRACE_FILE);
              databaseTraceFiles.execute();
           }
        }
    }//GEN-LAST:event_importBtnActionPerformed
   
